   public void preenche_tabela(){
       
       tblpeca.getColumnModel().getColumn(0).setPreferredWidth(5);
       tblpeca.getColumnModel().getColumn(1).setPreferredWidth(200);
       tblpeca.getColumnModel().getColumn(2).setPreferredWidth(30);
       tblpeca.getColumnModel().getColumn(3).setPreferredWidth(50);
       DefaultTableModel modelo =(DefaultTableModel)tblpeca.getModel();
       modelo.setNumRows(0);
       
       DaoPeca dao = new DaoPeca();
       
       List<Peca> pecas = dao.PreenchePecas();
       
       for(Peca peca: pecas){
           
           modelo.addRow(new Object[] {peca.getIdpeca(),
               peca.getNome(), peca.getPreco_unitario(), peca.getQt_estoque()});
       }
       }
   
