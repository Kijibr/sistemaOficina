   public void preenche_tabela(){
       
       tbcliente.getColumnModel().getColumn(0).setPreferredWidth(5);
       tbcliente.getColumnModel().getColumn(1).setPreferredWidth(200);
       tbcliente.getColumnModel().getColumn(2).setPreferredWidth(30);
       DefaultTableModel modelo =(DefaultTableModel)tbcliente.getModel();
       modelo.setNumRows(0);
       
       DaoCliente dao = new DaoCliente();
       
       List<Cliente> clientes = dao.PreencheClientes();
       
       for(Cliente cliente: clientes){
           
           modelo.addRow(new Object[] {cliente.getIdcliente(),
               cliente.getNome(), cliente.getTelefone()});
       }
       }
   
   
